package com.itheima.test;

public class Test12 {
    public static void main(String[] args) {
        //打印1-5
        for (int i = 1; i <= 5; i++) {
            //第一次循环时，i=1
            //第二次循环时，i=2
            System.out.println(i);
        }

        //打印5-1
        for (int i = 5; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
